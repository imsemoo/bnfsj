const fs = require("fs");
const { page } = require("./_chrome");

function legalPage(file, title, kicker, updated, intro, sections) {
  const toc = sections.map((s, i) => `              <li><a href="#l-${i + 1}">${s.h}</a></li>`).join("\n");
  const blocks = sections.map((s, i) => `              <h2 id="l-${i + 1}">${s.h}</h2>\n${s.body}`).join("\n");
  const body = `      <div class="container">
        <nav class="breadcrumb page-hero__breadcrumb" aria-label="مسار التصفّح" style="justify-content:flex-start">
          <a href="index.html">الرئيسية</a>
          <span class="breadcrumb__sep">›</span>
          <span class="breadcrumb__current">${title}</span>
        </nav>
        <header class="page-hero" style="text-align:start;padding-bottom:var(--space-6)">
          <span class="page-hero__kicker">${kicker}</span>
          <h1 class="page-hero__title">${title}</h1>
          <p class="page-hero__lead" style="margin-inline:0"><i class="fa-regular fa-clock" aria-hidden="true"></i> آخر تحديث: ${updated}</p>
        </header>
      </div>

      <section class="container page--tight">
        <div class="article">
          <article>
            <div class="prose">
              ${intro}
${blocks}
            </div>
          </article>
          <aside class="article-aside">
            <nav class="toc" data-toc aria-label="المحتويات">
              <h2 class="toc__title">المحتويات</h2>
              <ul class="toc__list">
${toc}
              </ul>
            </nav>
          </aside>
        </div>
      </section>`;
  fs.writeFileSync(file, page({ title, active: "", body, desc: title + " — بنفسج" }));
  console.log(file, "written");
}

